    public void ListarAutores() {
        List<Autor> ListaAu = audao.ListarAutores();
        modelo = (DefaultTableModel) TableAutor.getModel();
        Object[] ob = new Object[6];
        for (int i = 0; i < ListaAu.size(); i++) {
            ob[0] = ListaAu.get(i).getCodigo();
            ob[1] = ListaAu.get(i).getNombre();
            ob[2] = ListaAu.get(i).getApellido();
            
            modelo.addRow(ob);
        }
        TableAutor.setModel(modelo);
    }

    public void ListarLibros() {
        List<Libro> ListarLI = lidao.ListarLibros();
        modelo = (DefaultTableModel) TableLibro.getModel();
        Object[] ob = new Object[6];
        for (int i = 0; i < ListarLI.size(); i++) {
            ob[0] = ListarLI.get(i).getISBN();
            ob[1] = ListarLI.get(i).getTitulo();
            ob[2] = ListarLI.get(i).getAutor();
            ob[3] = ListarLI.get(i).getValorprestamo();
            modelo.addRow(ob);
        }
        TableLibro.setModel(modelo);
    }

    public void ListarPrestamos() {
        List<Prestamo> Listarpr = prdao.ListarPrestamos();
        modelo = (DefaultTableModel) TablePrestamo.getModel();
        Object[] ob = new Object[6];
        for (int i = 0; i < Listarpr.size(); i++) {
            ob[0] = Listarpr.get(i).getNuprestamo();
            ob[1] = Listarpr.get(i).getFechap();
            ob[2] = Listarpr.get(i).getDescripcion();
            ob[3] = Listarpr.get(i).getCodigoli();
            ob[4] = Listarpr.get(i).getCantidad();
            ob[5] = Listarpr.get(i).getFechae();
            modelo.addRow(ob);
        }
        TablePrestamo.setModel(modelo);
    }

    public void LimpiarTable() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }

