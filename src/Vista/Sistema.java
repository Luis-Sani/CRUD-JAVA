    private void pdf() {

        try {
            FileOutputStream archivo;
            File file = new File("src/Reportes/venta.pdf");
            archivo = new FileOutputStream(file);
            Document doc = new Document();
            PdfWriter.getInstance(doc, archivo);
            doc.open();
            Image img = Image.getInstance("src/Img/espe.png");

            Paragraph fecha = new Paragraph();
            Font negrita = new Font(Font.FontFamily.TIMES_ROMAN, 12, Font.BOLD, BaseColor.BLUE);
            fecha.add(Chunk.NEWLINE);
            Date date = new Date();
            fecha.add("Fecha: " + new SimpleDateFormat("dd-MM-yyyy").format(date) + "\n\n");

            PdfPTable Encabezado = new PdfPTable(4);
            Encabezado.setWidthPercentage(100);
            Encabezado.getDefaultCell().setBorder(0);
            float[] ColumnaEncabezado = new float[]{20f, 30f, 70f, 40f};
            Encabezado.setWidths(ColumnaEncabezado);
            Encabezado.setHorizontalAlignment(Element.ALIGN_LEFT);

            Encabezado.addCell(img);

            String nombrep = "Proyecto parcial uno";
            String motivo = "Generación de Reporte";

            Encabezado.addCell("");
            Encabezado.addCell(nombrep + "\n\n" + motivo);
            Encabezado.addCell(fecha);
            doc.add(Encabezado);

            Paragraph aut = new Paragraph();
            aut.add(Chunk.NEWLINE);
            aut.add("Datos de los autores" + "\n\n");
            doc.add(aut);

            PdfPTable tablaaut = new PdfPTable(3);
            tablaaut.setWidthPercentage(100);
            tablaaut.getDefaultCell().setBorder(0);
            float[] Columnaaut = new float[]{20f, 50f, 30f};
            tablaaut.setWidths(Columnaaut);
            tablaaut.setHorizontalAlignment(Element.ALIGN_LEFT);
            PdfPCell aut1 = new PdfPCell(new Phrase(""));
            PdfPCell aut2 = new PdfPCell(new Phrase(""));
            PdfPCell aut3 = new PdfPCell(new Phrase("\n"));
            aut1.setBorder(0);
            aut2.setBorder(0);
            aut3.setBorder(0);
            tablaaut.addCell(aut1);
            tablaaut.addCell(aut2);
            tablaaut.addCell(aut3);
            tablaaut.addCell(txtCodigoAutor.getText());
            tablaaut.addCell(txtNombreAutor.getText());
            tablaaut.addCell(txtApellidoAutor.getText());

            doc.add(tablaaut);

            // Mostrar Datos de las tablas
            PdfPTable tablacont = new PdfPTable(3);
            tablacont.setWidthPercentage(100);
            tablacont.getDefaultCell().setBorder(0);
            float[] Columnacont = new float[]{10f, 30f, 30f};
            tablacont.setWidths(Columnacont);
            tablacont.setHorizontalAlignment(Element.ALIGN_LEFT);
            PdfPCell cont1 = new PdfPCell(new Phrase("Codigo"));
            PdfPCell cont2 = new PdfPCell(new Phrase("Nombre"));
            PdfPCell cont3 = new PdfPCell(new Phrase("Apellido\n\n"));
            cont1.setBorder(0);
            cont2.setBorder(0);
            cont3.setBorder(0);
            tablacont.addCell(cont1);
            tablacont.addCell(cont2);
            tablacont.addCell(cont3);
            for (int i = 0; i < TableAutor.getRowCount(); i++) {
                String Codigo = TableAutor.getValueAt(i, 0).toString();
                String Nombre = TableAutor.getValueAt(i, 1).toString();
                String Apellido = TableAutor.getValueAt(i, 2).toString();

                tablacont.addCell(Codigo);
                tablacont.addCell(Nombre);
                tablacont.addCell(Apellido);
            }
            doc.add(tablacont);

            doc.close();
            archivo.close();
            Desktop.getDesktop().open(file);

        } catch (DocumentException | IOException e) {
            System.out.println(e.toString());
        }
    }

    
